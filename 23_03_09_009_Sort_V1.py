# Ordena la siguiente lista en orden alfabético descendente (de la letra 'z' a la 'a').
colores = ['rojo', 'azul', 'verde', 'amarillo', 'marrón',
          'lila', 'negro', 'rosa', 'blanco', 'naranja']

colores.sort(reverse=True) #La funcion Sort acomoda por orden alfabetico los elementos de la lista, si le ponemos reverse=True, los acomoda al reves (z-a)
print(colores) #Se imprime la lista

